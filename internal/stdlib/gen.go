// +build ignore

package main

import (
	"flag"
	"fmt"
	"log"
	"os"
	"os/exec"
	"strings"
)

const template = `// Automatically generated by go generate. DO NOT EDIT

package stdlib

var stdlibPackages = %#v
`

func main() {
	output := flag.String("output", "data.go", "output file")
	flag.Parse()

	pkgs := make(map[string]bool)
	cmd := exec.Command("go", "list", "std", "cmd")
	o, err := cmd.Output()
	if err != nil {
		log.Fatal(err)
	}
	for _, pkg := range strings.Fields(string(o)) {
		pkgs[pkg] = true
	}
	f, err := os.Create(*output)
	if err != nil {
		log.Fatal(err)
	}
	defer f.Close()
	fmt.Fprintf(f, template, pkgs)
}
